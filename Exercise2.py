# Ініціалізація змінних для суми парних та добутку непарних чисел
sum_even = 0
product_odd = 1

# Проходимо по діапазону від 0 до 20
for i in range(21):
    if i % 2 == 0:  # перевірка на парність
        sum_even += i
    else:
        product_odd *= i

# Виведення результатів
print(f"Сума парних чисел: {sum_even}")
print(f"Добуток непарних чисел: {product_odd}")
